//JS 04 - Input with prompt()

//collect input
var name = prompt("What is your name?");
var age = prompt("How old are you?");
var hometown = prompt("What is your hometown?");
var favNumber = prompt("What is your favorite number?");

//output with console.log()
console.log("Your name is " + name);
console.log("Your age is: " + age);
console.log("Your hometown is " + hometown);
console.log("Your favorite number is " + favNumber);

//output with alert()
alert("Your name is " + name);
alert("Your age is: " + age);
alert("Your hometown is " + hometown);
alert("Your favorite number is " + favNumber);
